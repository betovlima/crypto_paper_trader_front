const dictionaries = { en: {}, pt, es };

export function detectInitialLanguage() {
  const stored = window.localStorage.getItem("crypto-paper-trader-language");
  if (stored && dictionaries[stored]) return stored;
  const browser = String(navigator.language || "en").toLowerCase();
  if (browser.startsWith("pt")) return "pt";
  if (browser.startsWith("es")) return "es";
  return "en";
}

export function translate(language, source) {
  if (source === null || source === undefined) return source;
  const text = String(source);
  return dictionaries[language]?.[text] ?? text;
}

export function translateDynamicText(language, source) {
  if (!source) return source;
  const exact = translate(language, source);
  if (exact !== source) return exact;
  return source;
}
